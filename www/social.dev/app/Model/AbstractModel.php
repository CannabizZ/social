<?php
declare(strict_types=1);

namespace App\Model;

class AbstractModel implements ModelInterface
{
    protected int $id = 0;

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @param int $id
     * @return $this
     */
    public function setId(int $id): self
    {
        $this->id = $id;
        return $this;
    }
}